private void enviarCorreo(String correoDestino, File archivoAdjunto) {
    final String remitente = "hpcesar10@gmail.com"; // Tu correo real
    final String password = "cpoo osro awnb jqld"; // Contraseña generada por Google


    Properties props = new Properties();
    props.put("mail.smtp.auth", "true");
    props.put("mail.smtp.starttls.enable", "true");
    props.put("mail.smtp.host", "smtp.gmail.com");
    props.put("mail.smtp.port", "587");
    
    Session session = Session.getInstance(props, new jakarta.mail.Authenticator() {
        protected PasswordAuthentication getPasswordAuthentication() {
            return new PasswordAuthentication(remitente, password);
        }
    });

    try {
        Message message = new MimeMessage(session);
        message.setFrom(new InternetAddress(remitente));
        message.setRecipients(Message.RecipientType.TO, InternetAddress.parse(correoDestino));
        message.setSubject("Archivo adjunto desde la aplicación");

        MimeBodyPart messageBodyPart = new MimeBodyPart();
        messageBodyPart.setText("Se adjunta el archivo solicitado.");

        MimeBodyPart attachmentPart = new MimeBodyPart();
        attachmentPart.attachFile(archivoAdjunto);

        Multipart multipart = new MimeMultipart();
        multipart.addBodyPart(messageBodyPart);
        multipart.addBodyPart(attachmentPart);

        message.setContent(multipart);

        Transport.send(message);
        JOptionPane.showMessageDialog(null, "Correo enviado exitosamente.");
        
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al enviar el correo.");
    }
}

