    public void mostrar(String tabla){
    String sql = "SELECT * FROM " + tabla;
    conexionmysql con = new conexionmysql();
    Connection cn = con.estableceConexion();

    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("id");
    model.addColumn("email");
    model.addColumn("hora_entrada");
    model.addColumn("tipo");
    jTable1.setModel(model); // ← Corrección previa: usar jTable1, no "visor"

    String[] dato = new String[4];

    try {
        Statement st = cn.createStatement();
        ResultSet rs = st.executeQuery("SELECT email, hora_entrada, estado, tipo_usuario FROM bitacora ORDER BY hora_entrada DESC");

        while (rs.next()) {
            String email = rs.getString("email");
            String hora = rs.getString("hora_entrada");
            String estado = rs.getString("estado");
            String tipo = rs.getString("tipo_usuario");

            model.addRow(new Object[]{email, hora, estado, tipo});
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al cargar bitácora: " + e.getMessage());
    }
    }
    

